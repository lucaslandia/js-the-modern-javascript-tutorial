/* IDE

The term IDE (Integrated Development Environment) refers to a powerful editor with many features 
that usually operates on a “whole project.” As the name suggests, it’s not just an editor, but a 
full-scale “development environment.”

An IDE loads the project (which can be many files), allows navigation between files, provides 
autocompletion based on the whole project (not just the open file), and integrates with a version 
management system (like git), a testing environment, and other “project-level” stuff.


Lightweight editors

“Lightweight editors” are not as powerful as IDEs, but they’re fast, elegant and simple.

They are mainly used to open and edit a file instantly.

The main difference between a “lightweight editor” and an “IDE” is that an IDE works on a 
project-level, so it loads much more data on start, analyzes the project structure if needed and so
 on. A lightweight editor is much faster if we need only one file.

In practice, lightweight editors may have a lot of plugins including directory-level syntax analyzers 
and autocompleters, so there’s no strict border between a lightweight editor and an IDE. */